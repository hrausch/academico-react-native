// If/else
let numero = 10;
if (numero > 5) {
  console.log("O número é maior que 5");
} else {
  console.log("O número é menor ou igual a 5");
}

// Switch
let diaSemana = "domingo";
switch (diaSemana) {
  case "segunda":
    console.log("Dia de trabalho");
    break;
  case "domingo":
    console.log("Dia de descanso");
    break;
  default:
    console.log("Outro dia");
}